export const getIndustries = () => Object.keys(INDUSTRY_TAXONOMY).sort();

export const getSubIndustries = (industry: string) => {
  const node = INDUSTRY_TAXONOMY[industry];
  return node ? Object.keys(node).sort() : [];
};

export const getNiches = (industry: string, subIndustry: string) => {
  const node = INDUSTRY_TAXONOMY[industry];
  if (!node || Array.isArray(node)) return [];
  const subNode = node[subIndustry];
  return Array.isArray(subNode) ? subNode.sort() : [];
};
